# """
# Arithmetic Operations
# """

print(f'10 + 3 = {10 + 3} => Addition')  # 13
print(f'10 - 3 = {10 - 3} => Subtraction')  # 7
print(f'10 * 3 = {10 * 3} => Multiplication')  # 30
print(f'10 / 3 = {10 / 3} => Division (returns floating point value)')  # 3.33...
print(f'10 // 3 = {10 // 3} => Division (returns floor int value). This is exclusive in Python')  # 3
print(f'10 % 3 = {10 % 3} => Modulus (returns remainder of division)')  # 1
print(f'10 ** 3 = {10 ** 3} => Power (returns 10 to the power 3)')  # 1000

# AUGMENTED ASSIGNMENT OPERATOR:
x = 10
x += 3  # It is similar to x = x + 3. It is applicable to all arithmetic operators
print(x)
